    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here
        int userID = Integer.parseInt(txtId.getText());

        try
        {
            String sqlquery = "SELECT * FROM userlogin  WHERE userID = ?";
            pst = conn.prepareStatement(sqlquery);
            pst.setInt(1,userID);
            rs = pst.executeQuery();

            if(rs.next())
            {
                txtUser.setText(rs.getString("userName"));
                txtAdd.setText(rs.getString("uAdd"));
                txtTel.setText(rs.getString("uTel"));
                txtEmail.setText(rs.getString("uTel"));
                txtPass.setText(rs.getString("Password"));
            }
            else
            {
                txtUser.setText("");
                txtAdd.setText("");
                txtTel.setText("");
                txtEmail.setText("");
                txtPass.setText("");
            }
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

