    private void btnCalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalActionPerformed
        // TODO add your handling code here:
 
         try
        {
            int orderId = Integer.parseInt(txtId.getText());
            String itemName = (String)boxItemName.getSelectedItem();
            double price = Double.parseDouble(txtPrice.getText().trim());
            int    qnt   = (Integer) spinQnt.getValue();
            subId++;

            double subTotal = price * qnt;

            txtSubTotal.setText(String.valueOf(subTotal));

            txtTotal.setText(String.valueOf(total+=subTotal));   

            String tbData[] = {String.valueOf(subId),itemName,String.valueOf(price),String.valueOf(qnt),String.valueOf(subTotal)};
            DefaultTableModel tblModel = (DefaultTableModel)tbl.getModel();

            tblModel.addRow(tbData);
    
           String sqlquery = "INSERT INTO subOrders (subId,itemName,price,quantity,subTotal,orderId) VALUES (?,?,?,?,?,?)";
           
           pst = conn.prepareStatement(sqlquery);
           pst.setInt(1,subId);
           pst.setString(2, itemName);
           pst.setDouble(3, price);
           pst.setInt(4, qnt);
           pst.setDouble(5, subTotal);
           pst.setInt(6,orderId);
           
           pst.executeUpdate();
           
           String sqlquery1 = "INSERT INTO sales (itemName,price,qnt,subTotal,orderId) VALUES (?,?,?,?,?)";
           
           pst = conn.prepareStatement(sqlquery1);
           pst.setString(1, itemName);
           pst.setDouble(2, price);
           pst.setInt(3, qnt);
           pst.setDouble(4, subTotal);
           pst.setInt(5,orderId);
           
           pst.executeUpdate(); 
        }
        catch(SQLException e)
        {
           JOptionPane.showMessageDialog(null,e); 
        }
           catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null,"Select an item");
        }
    }//GEN-LAST:event_btnCalActionPerformed

