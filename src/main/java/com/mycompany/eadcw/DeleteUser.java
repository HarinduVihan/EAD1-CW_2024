    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try
        {

            int id = Integer.parseInt(txtId.getText().trim());

             String sqlquery = "SELECT * FROM userlogin  WHERE userID = ?";
            pst = conn.prepareStatement(sqlquery);
            pst.setInt(1,id);
            rs = pst.executeQuery();

            if(rs.next())
            {
               String q = "DELETE FROM userlogin WHERE userId = ?";
            pst = conn.prepareStatement(q);
            pst.setInt(1,id);
            pst.executeUpdate();

            JOptionPane.showMessageDialog(null,"User Deleted");

            txtId.setText("");
            txtUser.setText("");
            txtAdd.setText("");
            txtTel.setText("");
            txtEmail.setText("");
            txtPass.setText("");
            }
            else
            {
                JOptionPane.showMessageDialog(null,"Enter an existing User id ");
            }
            
        }
        catch(SQLException e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
         catch(NumberFormatException ex)
        {
            JOptionPane.showMessageDialog(null,"Enter an User id");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

