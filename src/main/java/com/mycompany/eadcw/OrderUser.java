    public void getModel(){
         try
        {
            String q = "SELECT itemName FROM items";
            pst = conn.prepareStatement(q);
            rs = pst.executeQuery();
            
           while(rs.next())
           {
               String itemName = rs.getString("itemName");
               boxItemName.addItem(itemName); 
           }         
                            
        }
        catch(SQLException e)
        {
             JOptionPane.showMessageDialog(null,e);
        }
         
        boxItemName.addActionListener(new ActionListener()
                {
                    public void actionPerformed(ActionEvent e)
                {
                    String iName = (String)boxItemName.getSelectedItem();
        
        try
        {
            String q = "SELECT itemPrice FROM items WHERE itemName = ?";
            pst = conn.prepareStatement(q);
            pst.setString(1, iName);
            rs = pst.executeQuery();
            
            if(rs.next())
            {
                 txtPrice.setText(rs.getString("itemPrice"));
            }
        }
        catch(SQLException ex)
        {
          JOptionPane.showMessageDialog(null,ex);   
        }
        
        }
       });
    }
